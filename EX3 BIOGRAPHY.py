#EX 3 BIOGRAPHY 

information = {'name' : 'Lianne', #key stores the value 'Lianne'
               'hometown':'Philippines', #key stores the value 'Philippines'
               'age' : 16 } #key stores the value '16'

print(f"{information['name']}\n{information['hometown']}\n{information['age']}") #prints the dictionary. f" is used to format the values into strings while /n is used to print the key-values into new lines 



